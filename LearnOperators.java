package week1.day1;

public class LearnOperators {
public static void main(String[] args) {
	int a=5;
	int b=7;
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(b/a);
	System.out.println(b%a);
	System.out.println(a*b);
	int c=5;
	c++;
	System.out.println(c++);
	System.out.println(++c);
	System.out.println(a==b);
	int d=5;
	d=d+5;
	System.out.println(d);
}
}
